    // Nivel desde el que la caché es compartida.
    public int cacheSepNivel()
    {
        int result = 0;
        
        switch(caches_separadas_cb.getSelectedItem().toString())
        {
            case "Todos":
                result = 1;
                break;
            case "Nivel 2":
                result = 2;
                break;
            case "Nivel 3":
                result = 3;
                break;
            case "Ninguno":
                result = 4;
                break;
        }
        
    	return result;
    }
    
    public void crearPestañas()
    {
        panelesCaches.removeAll();
        for (int i = 0; i < 6; i++)
            panelesCaches.addTab("Cache " + i, paneles_caches[i]);
    }
    
    public void eliminarPestañas()
    {
        int limite = panelesCaches.getComponentCount();
        int i = 0;
        while (i < limite)
        {
            if (!panelesCaches.isEnabledAt(i))
            {
                panelesCaches.remove(i);
                limite = panelesCaches.getComponentCount();
            }
            else
                i++;
        }
    }
    
